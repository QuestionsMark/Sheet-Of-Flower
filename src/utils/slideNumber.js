export function slideNumber(index) {
    switch (index) {
        case 0:
            return 'First slide';
        case 1:
            return 'Second slide';
        case 2:
            return 'Third slide';
        case 3:
            return 'Fourth slide';
        case 4:
            return 'Fifth slide';
        case 5:
            return 'Sixth slide';
        case 6:
            return 'Seventh slide';
        case 7:
            return 'Eighth slide';
        case 8:
            return 'Ninth slide';
        case 9:
            return 'Tenth slide';
        default:
            return;
    }
}